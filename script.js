let bancoDeAlunos = {};
let historico = [];

window.onload = function() {
    const cacheAlunos = localStorage.getItem('db_turmas_v12');
    if (cacheAlunos) {
        bancoDeAlunos = JSON.parse(cacheAlunos);
        Object.keys(alunosIniciais).forEach(turma => {
            if (!bancoDeAlunos[turma]) bancoDeAlunos[turma] = [];
            alunosIniciais[turma].forEach(nome => {
                if(!bancoDeAlunos[turma].includes(nome)) bancoDeAlunos[turma].push(nome);
            });
        });
    } else {
        bancoDeAlunos = Object.assign({}, alunosIniciais);
    }
    
    const cacheHistorico = localStorage.getItem('db_hist_v12');
    historico = cacheHistorico ? JSON.parse(cacheHistorico) : [];

    renderizarLista();
    document.getElementById('dataOcorrencia').valueAsDate = new Date();
    
    document.addEventListener('click', function(e) {
        if (!e.target.closest('#alunoNome') && !e.target.closest('#listaSugestoes')) {
            document.getElementById('listaSugestoes').style.display = 'none';
        }
    });
};

function filtrarNomesGlobais() {
    const input = document.getElementById('alunoNome');
    const listaDiv = document.getElementById('listaSugestoes');
    const termo = input.value.toUpperCase().trim();
    
    listaDiv.innerHTML = '';
    
    if (termo.length === 0) {
        listaDiv.style.display = 'none';
        document.getElementById('selectSerie').value = "";
        document.getElementById('selectTurmaLetra').value = "";
        return;
    }

    let filtrados = [];
    for (let turma in bancoDeAlunos) {
        bancoDeAlunos[turma].forEach(nome => {
            if (nome.includes(termo)) {
                filtrados.push({nome: nome, turma: turma});
            }
        });
    }
    
    if (filtrados.length > 0) {
        listaDiv.style.display = 'block';
        const maxResultados = filtrados.slice(0, 15);

        maxResultados.forEach(item => {
            const div = document.createElement('div');
            div.className = 'item-sugestao';
            
            const regex = new RegExp(`(${termo})`, 'gi');
            const nomeDestacado = item.nome.replace(regex, "<span style='color:var(--e63946); background:transparent; padding:0;'>$1</span>");

            div.innerHTML = `<div><strong>${nomeDestacado}</strong></div> <span class="item-turma">Turma: ${item.turma}</span>`;
            
            div.addEventListener('mousedown', function(e) { e.preventDefault(); selecionarAluno(item.nome, item.turma); });
            div.addEventListener('touchstart', function(e) { e.preventDefault(); selecionarAluno(item.nome, item.turma); });
            
            listaDiv.appendChild(div);
        });
    } else {
        listaDiv.style.display = 'block';
        const addDiv = document.createElement('div');
        addDiv.className = 'item-sugestao item-add';
        addDiv.innerHTML = `‚ö†Ô∏è N√£o achei na lista.<br><br>Preencha os campos "S√©rie" e "Turma" abaixo e toque AQUI para cadastrar o(a) <b>"${termo}"</b> para as pr√≥ximas vezes!`;
        addDiv.addEventListener('mousedown', function(e) { e.preventDefault(); registrarNovoAlunoForcado(termo); });
        addDiv.addEventListener('touchstart', function(e) { e.preventDefault(); registrarNovoAlunoForcado(termo); });
        listaDiv.appendChild(addDiv);
    }
}

function selecionarAluno(nome, turma) {
    document.getElementById('alunoNome').value = nome;
    document.getElementById('listaSugestoes').style.display = 'none';
    
    let matchSerie = "";
    let matchLetra = "";

    if (turma.startsWith("PRA")) {
        matchSerie = "PRA ";
        matchLetra = turma.replace("PRA", "").trim();
    } else {
        matchSerie = turma.substring(0, 2); 
        matchLetra = turma.substring(2).trim(); 
    }

    document.getElementById('selectSerie').value = matchSerie;
    document.getElementById('selectTurmaLetra').value = matchLetra;
}

function registrarNovoAlunoForcado(nome) {
    const serie = document.getElementById('selectSerie').value;
    const letra = document.getElementById('selectTurmaLetra').value;
    
    if (!serie || !letra) { 
        alert("Aten√ß√£o: Selecione primeiro a S√©rie e a Turma nas caixinhas abaixo antes de tentar adicionar!"); 
        return; 
    }

    const turmaFormatada = serie === "PRA " ? `PRA ${letra}` : `${serie}${letra}`;

    if (!bancoDeAlunos[turmaFormatada]) bancoDeAlunos[turmaFormatada] = [];
    bancoDeAlunos[turmaFormatada].push(nome);
    localStorage.setItem('db_turmas_v12', JSON.stringify(bancoDeAlunos));
    
    selecionarAluno(nome, turmaFormatada);
    alert(`O aluno ${nome} foi cadastrado e aparecer√° nas suas pr√≥ximas buscas!`);
}

