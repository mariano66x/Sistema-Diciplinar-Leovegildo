function restaurarBackup(e) {
    const reader = new FileReader();
    reader.onload = function(event) {
        try {
            const dados = JSON.parse(event.target.result);
            if(dados.alunos && dados.historico) {
                bancoDeAlunos = dados.alunos;
                historico = dados.historico;
                localStorage.setItem('db_turmas_v12', JSON.stringify(bancoDeAlunos));
                localStorage.setItem('db_hist_v12', JSON.stringify(historico));
                alert("Backup restaurado com sucesso!");
                location.reload();
            } else { alert("Arquivo inv√°lido."); }
        } catch(err) { alert("Erro ao ler o arquivo."); }
    };
    reader.readAsText(e.target.files[0]);
}
