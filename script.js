function adicionarOcorrencia() {
    const nome = document.getElementById('alunoNome').value.toUpperCase().trim();
    const serie = document.getElementById('selectSerie').value;
    const letra = document.getElementById('selectTurmaLetra').value;
    const dataRaw = document.getElementById('dataOcorrencia').value;
    const responsavel = document.getElementById('selectResponsavel').value;
    
    let desc = document.getElementById('selectOcorrencia').value;
    if (desc === 'OUTRO') desc = document.getElementById('ocorrenciaManual').value.trim();

    if (!nome) { alert("Digite ou pesquise o nome do aluno!"); return; }
    if (!serie || !letra) { alert("A S√©rie e Turma devem estar preenchidas!"); return; }
    if (!dataRaw) { alert("Selecione a Data."); return; }
    if (!responsavel) { alert("Selecione o Respons√°vel pela notifica√ß√£o!"); return; }
    if (!desc) { alert("Selecione ou digite o motivo da ocorr√™ncia."); return; }

    const turma = serie === "PRA " ? `PRA ${letra}` : `${serie}${letra}`;
    
    if (!bancoDeAlunos[turma]) bancoDeAlunos[turma] = [];
    if (!bancoDeAlunos[turma].includes(nome)) bancoDeAlunos[turma].push(nome);
    
    const registro = { 
        id: Date.now(), 
        data: dataRaw, 
        nome: nome, 
        turma: turma, 
        responsavel: responsavel,
        desc: desc 
    };

    historico.unshift(registro); 
    localStorage.setItem('db_turmas_v12', JSON.stringify(bancoDeAlunos));
    localStorage.setItem('db_hist_v12', JSON.stringify(historico));
    
    renderizarLista();
    
    document.getElementById('alunoNome').value = "";
    document.getElementById('selectSerie').value = "";
    document.getElementById('selectTurmaLetra').value = "";
    document.getElementById('selectResponsavel').value = "";
    document.getElementById('selectOcorrencia').value = "";
    document.getElementById('ocorrenciaManual').value = "";
    document.getElementById('ocorrenciaManual').style.display = "none";
    document.getElementById('listaSugestoes').style.display = 'none';
    
    alert("Ocorr√™ncia Registada com Sucesso! ‚úÖ");
}

function deletar(id) {
    if(confirm("Deseja apagar este registo?")) {
        historico = historico.filter(item => item.id !== id);
        localStorage.setItem('db_hist_v12', JSON.stringify(historico));
        renderizarLista();
    }
}

function renderizarLista() {
    const container = document.getElementById('listaOcorrencias');
    container.innerHTML = "";
    if(historico.length === 0) {
        container.innerHTML = "<p style='text-align:center; color:#999; margin-top:20px;'>Nenhum registo no hist√≥rico.</p>";
        return;
    }
    historico.forEach(item => {
        const dataParts = item.data.split('-');
        const dataFmt = `${dataParts[2]}/${dataParts[1]}/${dataParts[0]}`;
        const responsavelLabel = item.responsavel ? item.responsavel : "N√£o informado";

        const card = document.createElement('div');
        card.className = 'card-ocorrencia';
        card.innerHTML = `
            <div class="card-top">
                <span class="card-date">üìÖ ${dataFmt}</span>
                <span class="card-turma">${item.turma}</span>
            </div>
            <strong class="card-nome">${item.nome}</strong>
            <div class="card-responsavel">üë§ ${responsavelLabel}</div>
            <div class="card-desc">${item.desc}</div>
            <div class="card-footer">
                <button class="btn-danger" onclick="deletar(${item.id})">üóëÔ∏è Excluir</button>
            </div>
        `;
        container.appendChild(card);
    });
}

function exportarExcel() {
    if(historico.length === 0) { alert("N√£o h√° registos."); return; }
    let html = "<table border='1'><tr><th>Data</th><th>Nome do Aluno</th><th>Turma</th><th>Respons√°vel</th><th>Motivo</th></tr>";
    historico.forEach(h => {
        const resp = h.responsavel ? h.responsavel : "";
        html += `<tr><td>${h.data}</td><td>${h.nome}</td><td>${h.turma}</td><td>${resp}</td><td>${h.desc}</td></tr>`;
    });
    html += "</table>";
    const a = document.createElement('a');
    const dataAtual = new Date().toLocaleDateString().replace(/\//g, '-');
    a.href = 'data:application/vnd.ms-excel,' + encodeURIComponent(html);
    a.download = `Relatorio_Ocorrencias_${dataAtual}.xls`;
    a.click();
}

function fazerBackup() {
    const dados = JSON.stringify({ alunos: bancoDeAlunos, historico: historico });
    const blob = new Blob([dados], {type: "application/json"});
    const a = document.createElement('a');
    a.href = URL.createObjectURL(blob);
    a.download = `BACKUP_ESCOLA_${Date.now()}.json`;
    a.click();
}

